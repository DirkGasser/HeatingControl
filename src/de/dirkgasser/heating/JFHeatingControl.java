    private void jBnewSelAfterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBnewSelAfterActionPerformed
        // TODO add your handling code here:
        HeatProgramStep newHeatStep;  
        if (displayedHeatDay.getCurrentStep() == null) {
            newHeatStep = new HeatProgramStep(LocalTime.of(0, 0),
                                              LocalTime.of(23,59),      
                                              heatingProgram.getDefaultHereTemp(),
                                              heatingProgram.getDefaultAbsentTemp());
            displayedHeatDay.addHeatStep(newHeatStep);
        } else {
            if (displayedHeatDay.getCurrentStep().getEndTime().isBefore(LocalTime.of(23,59))) {
                if (heatingProgram.getCurrentHeatDay().readNextStep() != null) {
                    if (displayedHeatDay.getCurrentStep().getEndTime().isBefore(heatingProgram.getCurrentHeatDay().readNextStep().getStartTime())) {
                        newHeatStep = new HeatProgramStep(displayedHeatDay.getCurrentStep().getEndTime().plusMinutes(1),
                                              heatingProgram.getCurrentHeatDay().readNextStep().getStartTime().minusMinutes(1),      
                                              heatingProgram.getDefaultHereTemp(),
                                              heatingProgram.getDefaultAbsentTemp());
                        displayedHeatDay.addHeatStep(newHeatStep);
                    } else {
                        JOptionPane.showMessageDialog(this, "Schritt danach grenzt direkt an");
                    }
                } else {
                    newHeatStep = new HeatProgramStep(displayedHeatDay.getCurrentStep().getEndTime().plusMinutes(1),
                                              LocalTime.of(23,59),      
                                              heatingProgram.getDefaultHereTemp(),
                                              heatingProgram.getDefaultAbsentTemp());
                    displayedHeatDay.addHeatStep(newHeatStep);
                }
            }
            
        }
        if (displayedHeatDay.getCurrentStep() != null) {
            jLstartTime.setText(displayedHeatDay.getCurrentStep().getStartTimeAsString());
            jLendTime.setText(displayedHeatDay.getCurrentStep().getEndTimeAsString());
            jLTempAbsent.setText(displayedHeatDay.getCurrentStep().getAbsenTempAsString());
            jLtempHere.setText(displayedHeatDay.getCurrentStep().gethHereTempAsString());
       }
    }//GEN-LAST:event_jBnewSelAfterActionPerformed

    private void jLstartTimeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLstartTimeMouseClicked
        // TODO add your handling code here:
        LocalTime minTime;
        LocalTime maxTime;
        if (displayedHeatDay.getCurrentStep() == null || displayedHeatDay.getCurrentStep().getStartTime() == null) {
            JOptionPane.showMessageDialog(this, "Zuerst neuen Schritt anlegen");
        } else {
            if (displayedHeatDay.readPrevStep() == null) {
                minTime = LocalTime.of(0, 0); 
            } else {
                minTime = displayedHeatDay.readPrevStep().getEndTime();
            }
            maxTime = displayedHeatDay.getCurrentStep().getEndTime(); 

            final JDTimePopup popupFrame = new JDTimePopup(this, true,displayedHeatDay.getCurrentStep().getStartTime(),
                                                            minTime, maxTime);
            displayedHeatDay.getCurrentStep().setStartTime(popupFrame.getTimeBack());
            jLstartTime.setText(displayedHeatDay.getCurrentStep().getStartTimeAsString());
        }     
    }//GEN-LAST:event_jLstartTimeMouseClicked

